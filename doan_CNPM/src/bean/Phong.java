package bean;

public class Phong {
private String maphong;
private String ten;
public String getMaphong() {
	return maphong;
}
public void setMaphong(String maphong) {
	this.maphong = maphong;
}
public String getTen() {
	return ten;
}
public void setTen(String ten) {
	this.ten = ten;
}
public Phong(String maphong, String ten) {
	super();
	this.maphong = maphong;
	this.ten = ten;
}


}
